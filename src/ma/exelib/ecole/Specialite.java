public class Specialite {
	//propriétés
	private int id;
	private String code, libelle;
	
	//constructeurs
	public Specialite(){
		id = 0;
		code = "";
		libelle = "";
	}
	public Specialite(int cId, String cCode, String cLibelle){
		id = cId;
		code = cCode;
		libelle = cLibelle;
	}
	
	// accesseurs mutateurs
	
}
